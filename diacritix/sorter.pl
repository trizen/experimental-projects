sub replacer {
    my $x = shift;
    $x =~ s/\x{00CE}/Î/g;
    $x =~ s/\x{00EE}/î/g;
    $x =~ s/\x{0218}|\x{015E}/Ș/g;
    $x =~ s/\x{0219}|\x{00AA}|\x{015F}/ș/g;
    $x =~ s/\x{00E3}/ă/g;
    $x =~ s/\x{00E2}/â/g;
    $x =~ s/\x{0102}/Ă/g;
    $x =~ s/\x{00C2}/Â/g;
    $x =~ s/\x{021B}|\x{0163}|\x{00FE}/ț/g;
    $x =~ s/\x{00DE}|\x{0162}|\x{021A}/Ț/g;
    return $x;
}

sub strip {
    my $y = shift;
    $y =~ s/\x{00CE}/I/g;
    $y =~ s/\x{00EE}/i/g;
    $y =~ s/\x{0218}|\x{015E}/S/g;
    $y =~ s/\x{0219}|\x{00BA}|\x{00AA}|\x{015F}/s/g;
    $y =~ s/\x{0103}|\x{00E3}|\x{00E2}/a/g;
    $y =~ s/\x{0102}|\x{00C2}/A/g;
    $y =~ s/\x{021B}|\x{0163}|\x{00FE}/t/g;
    $y =~ s/\x{00DE}|\x{0162}|\x{021A}/T/g;
    return $y;
}
close OUT;

