    if ($A > $B) {
        return;
    }

    my $max_p = sqrtint($B);

    sub ($m, $L, $lo, $k) {

        my $hi = vecmin($max_p, rootint(divint($B, $m), $k));

        if ($lo > $hi) {
            return;
        }

        if ($k == 1) {

            $lo = vecmax($lo, divceil($A, $m));
            $lo > $hi && return;

            my $t = mulmod(invmod($m, $L) // (return), -1, $L);
            $t > $hi && return;
            $t += $L while ($t < $lo);

            for (my $p = $t ; $p <= $hi ; $p += $L) {
                is_prime($p) || next;
                $m % $p == 0 and next;
                my $n = $m * $p;
                if (($n + 1) % ($p + 1) == 0) {
                    $callback->($n);
                    if ($n < $B) {
                        $B = $n;
                    }
                }
            }

            return;
        }

        foreach my $p (@{primes($lo, $hi)}) {

            if ($m % $p == 0) {
                next;
            }

            gcd($m, $p+1) == 1 or next;

            __SUB__->($m*$p, lcm($L, $p+1), $p+1, $k - 1);
        }
      }
      ->($m, $L, 3, $k);

    return 1;
}

