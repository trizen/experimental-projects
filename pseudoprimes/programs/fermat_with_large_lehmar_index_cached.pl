#!/usr/bin/perl

# Fermat pseudoprimes with large Lehmar index: n / phi(n).

# See also:
#   "A note on Lehmer's Totient Problem" by Richard G.E. Pinch

use 5.020;
use strict;
use warnings;
use experimental qw(signatures);

use Storable;

use Math::GMPz;
use Math::GMPq;
use Math::MPFR;

use ntheory qw(:all);
use Math::Prime::Util::GMP;
use experimental qw(signatures);

my $fermat_file = "cache/factors-fermat.storable";
my $fermat = retrieve($fermat_file);

sub my_euler_phi ($factors) {   # assumes n is squarefree
    Math::GMPz->new(Math::Prime::Util::GMP::vecprod(map{ ($_ < ~0) ? ($_-1) : Math::Prime::Util::GMP::subint($_, 1) } @$factors));
}

my $q = Math::GMPq->new(0);
my $f = Math::MPFR->new(0);

my @results;

while (my ($key, $value) = each %$fermat) {

    my @factors = split(' ', $value);

    scalar(@factors) >= 9 or next;
    #$factors[-1] < ~0 or next;

    my $phi = my_euler_phi(\@factors);

    Math::GMPq::Rmpq_set_str($q, $key, 10);
    Math::GMPq::Rmpq_set_den($q, $phi);
    Math::GMPq::Rmpq_canonicalize($q);

    Math::MPFR::Rmpfr_set_q($f, $q, 0);

    if (Math::MPFR::Rmpfr_cmp_d($f, 2.36) > 0) {
        push @results, [Math::MPFR::Rmpfr_get_d($f, 0), $key];
    }
}

@results = sort { ($a->[0] <=> $b->[0]) || (length($a->[1]) <=> length($b->[1])) } @results;

foreach my $r (@results) {
    printf("%.4f Lehmer index: %s\n", $r->[0], $r->[1]);
}

__END__

